package com.gustavo.termus;

public class Jogador {

    String nome;

    String foto;

    int vitorias;

    int streak;

    public Jogador() {
    }

    public String getNome() {
        return nome;
    }

    public String getFoto() {
        return foto;
    }

    public int getVitorias() {
        return vitorias;
    }

    public int getStreak() {
        return streak;
    }
}